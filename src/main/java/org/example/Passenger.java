package org.example;

public class Passenger {

    private String title;
    private String name;
    private long id;
    private long phone;
    private int age;

    public Passenger(String title, String name, long id, long phone, int age) {
        setTitle(title);
        setName(name);
        setId(id);
        setPhone(phone);
        setAge(age);
    }
}
